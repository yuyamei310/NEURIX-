'use client'

import { archetypeBadgeLabel } from '@/lib/classifier'
import type { AtlasResult } from '@/types/atlas'

interface DNACardProps {
  result: AtlasResult
  canvasDataUrl?: string
}

export function DNACard({ result, canvasDataUrl }: DNACardProps) {
  const top3 = result.coach
    ? result.coach.sport_recommendations.slice(0, 3).map((r) => r.sport)
    : ['—', '—', '—']

  const oldestTwin = result.soul_twins[0]

  return (
    <div
      id="dna-card"
      style={{
        position: 'fixed',
        top: -9999,
        left: -9999,
        width: 540,
        height: 675,
        background: '#ffffff',
        fontFamily: '-apple-system, Helvetica Neue, sans-serif',
        display: 'flex',
        flexDirection: 'column',
        padding: '40px',
        border: '0.5px solid #e5e5e7',
      }}
    >
      {/* Header */}
      <div style={{ display: 'flex', justifyContent: 'space-between', alignItems: 'flex-start', marginBottom: 24 }}>
        <div>
          <div style={{ fontFamily: 'ui-monospace, monospace', fontSize: 10, letterSpacing: '0.14em', textTransform: 'uppercase', color: '#86868b', marginBottom: 4 }}>
            TEAM USA · ATHLETE INTELLIGENCE
          </div>
          <div style={{ fontSize: 28, fontWeight: 700, letterSpacing: '-0.04em', color: '#1d1d1f' }}>ATLAS</div>
        </div>
        <div style={{ fontFamily: 'ui-monospace, monospace', fontSize: 10, letterSpacing: '0.14em', textTransform: 'uppercase', color: '#86868b' }}>
          {new Date().getFullYear()}
        </div>
      </div>

      {/* 3D canvas */}
      {canvasDataUrl && (
        <div style={{ width: '100%', height: 240, background: '#fafafa', borderRadius: 10, overflow: 'hidden', marginBottom: 24 }}>
          {/* eslint-disable-next-line @next/next/no-img-element */}
          <img
            id="dna-card-3d-img"
            src={canvasDataUrl}
            alt="3D body"
            style={{ width: '100%', height: '100%', objectFit: 'contain' }}
          />
        </div>
      )}

      {/* Archetype */}
      <div style={{ marginBottom: 20 }}>
        <div style={{ fontFamily: 'ui-monospace, monospace', fontSize: 10, letterSpacing: '0.14em', textTransform: 'uppercase', color: '#86868b', marginBottom: 6 }}>
          ARCHETYPE
        </div>
        <div style={{ display: 'flex', alignItems: 'center', gap: 10 }}>
          <span style={{ fontSize: 22, fontWeight: 700, letterSpacing: '-0.03em', color: '#1d1d1f' }}>
            {result.archetype.archetype.charAt(0).toUpperCase() + result.archetype.archetype.slice(1)}
          </span>
          <span style={{
            fontFamily: 'ui-monospace, monospace', fontSize: 10, letterSpacing: '0.14em',
            textTransform: 'uppercase', border: '0.5px solid #d0d0d5', borderRadius: 999,
            padding: '2px 8px', color: '#1d1d1f',
          }}>
            {archetypeBadgeLabel(result.archetype.archetype)}
          </span>
        </div>
      </div>

      {/* Sports */}
      <div style={{ marginBottom: 20 }}>
        <div style={{ fontFamily: 'ui-monospace, monospace', fontSize: 10, letterSpacing: '0.14em', textTransform: 'uppercase', color: '#86868b', marginBottom: 6 }}>
          TOP SPORTS
        </div>
        <div style={{ display: 'flex', gap: 8 }}>
          {top3.map((s, i) => (
            <span key={i} style={{ fontSize: 12, color: '#1d1d1f', border: '0.5px solid #e5e5e7', borderRadius: 6, padding: '3px 8px' }}>{s}</span>
          ))}
        </div>
      </div>

      {/* Soul twin */}
      {oldestTwin && (
        <div style={{ marginBottom: 20 }}>
          <div style={{ fontFamily: 'ui-monospace, monospace', fontSize: 10, letterSpacing: '0.14em', textTransform: 'uppercase', color: '#86868b', marginBottom: 6 }}>
            SOUL TWIN ERA
          </div>
          <div style={{ fontSize: 13, color: '#6e6e73' }}>{oldestTwin.era} · {oldestTwin.games_type}</div>
        </div>
      )}

      {/* Footer */}
      <div style={{ marginTop: 'auto', fontFamily: 'ui-monospace, monospace', fontSize: 10, letterSpacing: '0.14em', textTransform: 'uppercase', color: '#86868b' }}>
        Generated by ATLAS · Powered by Gemini
      </div>
    </div>
  )
}
